// Место для первой задачи
function calculateVolumeAndArea(rebLength) {
    if(typeof(rebLength) !== 'number' || Number.isInteger(rebLength) === false || rebLength < 1) {
        return "При вычислении произошла ошибка";
    } 
    let V = rebLength * rebLength * rebLength;
    let S = 6 * rebLength * rebLength;
    
    return `Объем куба: ${V}, площадь всей поверхности: ${S}`;
}
calculateVolumeAndArea(3);

